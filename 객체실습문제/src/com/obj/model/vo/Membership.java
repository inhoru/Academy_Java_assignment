public class Membership {
	public String id; 
	public String pw; 
	public String email; 
	public int age; 
	public String hobby ; 
	
	
	public Membership(String id,String pw, String email,int age,String hobby) {
		this.id=id;
		this.pw=pw;
		this.email=email;
		this.age=age;
		this.hobby=hobby;
	}
	
		
}
